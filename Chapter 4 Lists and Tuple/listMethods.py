friends= ["Apple", "Aakash", 5, 354.342, False] #We can add any type of data in a list
print(friends)

friends.append("Blood") #Adds an element at the end of the list
print(friends)

friends.reverse() #Reverses the list
print(friends)

friends.insert(1, "Candy") #Inserts an element at a specific index
print(friends)

friends.remove("Apple") #Removes the first occurrence of the element
print(friends)

